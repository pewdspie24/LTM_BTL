    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if (!GameClient.isLogin) {
            if (!"".equals(this.txtUsername.getText()) && !"".equals(this.txtPassword.getText())) {
                try {
                    String username = this.txtUsername.getText();
                    String password = this.txtPassword.getText();
                    ObjectOutputStream oos = new ObjectOutputStream(GameClient.socket.getOutputStream());
                    Request req = new Request(2);
                    req.action = "login";
                    req.data[0] = username;
                    req.data[1] = password;
                    oos.writeObject(req);
                    oos.flush();
                } catch (IOException ex) {
                    System.out.println(ex);
                    Logger.getLogger(GUI_Login.class.getName()).log(Level.SEVERE, null, ex);
                }

            } else {
                JOptionPane.showMessageDialog(this, "Please, enter username and password", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Player is online");
        }

    }//GEN-LAST:event_btnLoginActionPerformed

